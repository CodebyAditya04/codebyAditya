# Formatting a table using center(), ljust(), and rjust()

# Table headers
print("Item".ljust(15), "Price".rjust(10), "Quantity".center(10))

# Data rows
print("Apples".ljust(15), "120".rjust(10), "5".center(10))
print("Bananas".ljust(15), "60".rjust(10), "12".center(10))
print("Mangoes".ljust(15), "150".rjust(10), "7".center(10))