students = {"Aditya":100, "Aditi":98, "Rohit":99, "kamal":96}

# Looping over keys
for key in students:
    print("Looping over keys:", key)

# Looping over values
for value in students.values():
    print("Looping over values:", value)

# Looping over key-value pairs
for key, value in students.items():
    print("\n Looping over key-value pairs in dictionary:",)
    print(key, ":", value)